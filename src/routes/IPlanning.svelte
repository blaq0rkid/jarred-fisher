    <h1>Incapacity Planning</h1>
    <p>Incapacity planning is just as important, if not more important, than death planning. If you are in a serious car accident, you may not be able to take care of your own affairs for a matter of months or years.  Who is going to pay the mortgage?  Who is going to make medical decisions for you?  This is what incapacity planning is designed to accomplish.</p>  

    <p>There are strategies that give you control over your life even when you are incapacitated. We consider an incapacity plan as part of the basic estate plan that everyone should have.  We recommend that you consider the following types of incapacity planning documents:</p>
<ul>
    <li>Financial/Legal Durable Power of Attorney
    </li>
    <li>Medical Power of Attorney
    </li>
    <li>Directive to Physicians and Family or Surrogates
    </li>
    <li>HIPAA Release
    </li>
    <li>Declaration of Guardian in the Event of Later Incapacity or Later need of Guardian
    </li>
</ul>
<p>The appointment of an agent under a power of attorney will allow your agent to act on your behalf with respect to asset management and health care decisions. The Durable Power of Attorney addresses the management of your finances.  A Medical Power of Attorney addresses the management of your medical decisions. The Directive to Physicians and Family or Surrogates communicates your decisions about the level of life-prolonging measures to be taken to keep you alive. You might have heard of the Terri Schiavo case in Florida. Ms. Schiavo did not have a Directive to Physicians.
</p>
<p>An often overlooked planning tool is the Declaration of Guardian in the Event of Later Incapacity or Later need of Guardian. The most litigated part of a guardianship proceeding centers on who will become the guardian. It will avoid much heartache and potential litigation if you make your desires known ahead of time.  Our goal is to prevent future family disputes.  We use several estate planning tools to accomplish that goal.
</p>
