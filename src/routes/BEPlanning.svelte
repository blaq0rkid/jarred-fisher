    <h1>Basic Estate Planning</h1>
    <p>Everyone has an estate. Some estates are larger, $10 million or more. Some estates are smaller, $75,000 or less. Most are somewhere between those two numbers. Estate planning is important for everyone, regardless of estate size or value. You have worked hard for what you have and if you have more, you have more to lose. If you have less, you can’t afford to lose what you have.</p>

    <p>Your estate is made up of everything you own— your car, home, other real estate, checking and savings accounts, investments, life insurance, furniture, and personal possessions. We talk about estate planning in terms of protecting your FAMILY, your LIFE, and your FUTURE. We offer a free workshop on How to Protect Your Family, Your Life, Your Future.  
    </p>
    <p>How do you protect your FAMILY? You protect your family by having instructions in place so that your wishes are known after you die. Our main goal in estate planning is to avoid future family disputes. Nothing tears a family apart like a dispute over the estate of a loved one. 
    </p>
    <p>How do you protect your LIFE? You protect your life by having instructions in place that are in effect while you are still alive. Who is going to pay the mortgage and take care of your home if you are suddenly injured in a car accident? Who is going to make medical decisions for you if you are unconscious? Again, having a plan in place avoids family disputes about how things are taken care of and who is taking care of them. 
    </p>
    <p>How do you protect your FUTURE? You protect your future by having an integrated plan in place that operates well while you are alive and after you are gone. One of the best ways to integrate a plan to avoid family disputes is to have a revocable living trust plan. We offer several trust options that provide various levels of protection for yourself and for the loved ones who will inherit from you.
    </p>
